<?php

namespace Cyberdean\Security\BotDetectBundle\Entity\Security;

use Doctrine\ORM\Mapping as ORM;

/**
 * BadUrl
 *
 * @ORM\Table(name="security_bad_url")
 * @ORM\Entity(repositoryClass="Cyberdean\Security\BotDetectBundle\Repository\Security\BadUrlRepository")
 */
class BadUrl
{
    /**
     * @var int
     *
     * @ORM\Column(name="id", type="integer")
     * @ORM\Id
     * @ORM\GeneratedValue(strategy="AUTO")
     */
    private $id;

    /**
     * @var string
     *
     * @ORM\Column(name="url", type="string", length=1000, unique=true)
     */
    private $url;


    /**
     * Get id
     *
     * @return int
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set url
     *
     * @param string $url
     *
     * @return BadUrl
     */
    public function setUrl($url)
    {
        $this->url = $url;

        return $this;
    }

    /**
     * Get url
     *
     * @return string
     */
    public function getUrl()
    {
        return $this->url;
    }
}

